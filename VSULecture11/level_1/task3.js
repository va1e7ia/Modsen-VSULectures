function MathHelper() {}

MathHelper.add = function (a, b) {
  return a + b;
};

MathHelper.subtract = function (a, b) {
  return a - b;
};

MathHelper.multiply = function (a, b) {
  return a * b;
};

console.log(MathHelper.add(1, 2));
console.log(MathHelper.subtract(3, 2));
console.log(MathHelper.multiply(4, 2));
